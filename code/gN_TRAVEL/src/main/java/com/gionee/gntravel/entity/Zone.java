package com.gionee.gntravel.entity;
public class Zone {
	private int id;
	private String zone;
	private String zoneName;
	private String zoneEName;
	private int city;
	private String zoneDesc;
	private String zoneMapuse;
	private String zoneRange;
	private String zoneMapPic;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getZone() {
		return zone;
	}
	public void setZone(String zone) {
		this.zone = zone;
	}
	public String getZoneName() {
		return zoneName;
	}
	public void setZoneName(String zoneName) {
		this.zoneName = zoneName;
	}
	public String getZoneEName() {
		return zoneEName;
	}
	public void setZoneEName(String zoneEName) {
		this.zoneEName = zoneEName;
	}
	public int getCity() {
		return city;
	}
	public void setCity(int city) {
		this.city = city;
	}
	public String getZoneDesc() {
		return zoneDesc;
	}
	public void setZoneDesc(String zoneDesc) {
		this.zoneDesc = zoneDesc;
	}
	public String getZoneMapuse() {
		return zoneMapuse;
	}
	public void setZoneMapuse(String zoneMapuse) {
		this.zoneMapuse = zoneMapuse;
	}
	public String getZoneRange() {
		return zoneRange;
	}
	public void setZoneRange(String zoneRange) {
		this.zoneRange = zoneRange;
	}
	public String getZoneMapPic() {
		return zoneMapPic;
	}
	public void setZoneMapPic(String zoneMapPic) {
		this.zoneMapPic = zoneMapPic;
	}

}
