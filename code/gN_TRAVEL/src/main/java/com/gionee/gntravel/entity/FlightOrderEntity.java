package com.gionee.gntravel.entity;

public class FlightOrderEntity {
	private String orderFilghtNum;
	private String orderAirPort;
	private String orderId;
	private String orderTakeOffDate;
	private String orderPrice;
	private String orderState;

	public String getOrderFilghtNum() {
		return orderFilghtNum;
	}

	public void setOrderFilghtNum(String orderFilghtNum) {
		this.orderFilghtNum = orderFilghtNum;
	}

	public String getOrderAirPort() {
		return orderAirPort;
	}

	public void setOrderAirPort(String orderAirPort) {
		this.orderAirPort = orderAirPort;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getOrderTakeOffDate() {
		return orderTakeOffDate;
	}

	public void setOrderTakeOffDate(String orderTakeOffDate) {
		this.orderTakeOffDate = orderTakeOffDate;
	}

	public String getOrderPrice() {
		return orderPrice;
	}

	public void setOrderPrice(String orderPrice) {
		this.orderPrice = orderPrice;
	}

	public String getOrderState() {
		return orderState;
	}

	public void setOrderState(String orderState) {
		this.orderState = orderState;
	}

}
